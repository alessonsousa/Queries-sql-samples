SELECT
	SPSUSUARIO.NOME, SPSCONTROLEDISTRIBUICAOPROVA.NUMEROINSCRICAO, SPSOPCAOINSCRITO.IDPS, SPSOPCAOINSCRITO.CODUSUARIOPS, SPSOPCAOINSCRITO.STATUS

FROM SPSOPCAOINSCRITO
	INNER JOIN SPSUSUARIO ON
				SPSUSUARIO.CODUSUARIOPS = SPSOPCAOINSCRITO.CODUSUARIOPS
	INNER JOIN SPSCONTROLEDISTRIBUICAOPROVA ON
				SPSCONTROLEDISTRIBUICAOPROVA.IDPS = SPSOPCAOINSCRITO.IDPS
			AND SPSCONTROLEDISTRIBUICAOPROVA.CODUSUARIOPS = SPSOPCAOINSCRITO.CODUSUARIOPS 
WHERE 
		SPSOPCAOINSCRITO.IDPS IN (195, 201, 204)
	AND SPSOPCAOINSCRITO.STATUS NOT IN (7, 8, 5)
	AND SPSOPCAOINSCRITO.CODUSUARIOPS IN (6558,10950,10955,10993,10995,10964,10983,10973,5907,10972,10990,7223,10904,10477,10954,10541,10958,6912,9756,10952,10976,10959,10909,10970,1483,1483,307,10994,10968)

ORDER BY
	SPSOPCAOINSCRITO.STATUS DESC, SPSUSUARIO.NOME